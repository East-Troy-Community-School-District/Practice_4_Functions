def cross():
    '''
    Prints a cross pattern.
    '''
    print(" * * ")
    print("  *  ")
    print(" * * ")


def rectangle():
    '''
    Prints a rectangle pattern.
    '''
    print("*****")
    print("*****")


def line():
    '''
    Prints a line pattern.
    '''
    print("  *  ")
    print("  *  ")
    print("  *  ")


rectangle()
cross()
rectangle()
print()

line()
rectangle()
line()
print()

